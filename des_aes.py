def permute(bits, table):
    return "".join(bits[i-1] for i in table)
def left_shift(bits):
    return bits[1:] + bits[0]
def xor(a,b):
    result=""
    for i in range(len(a)):
        result += "0" if a[i]==b[i] else "1"
    return result
def sbox(bits,box):
    row = int(bits[0]+bits[3],2)
    col = int(bits[1]+bits[2],2)
    val = box[row][col]
    return format(val,"02b")
def run_sdes():
    pt = input("Enter Plain Text (8 bits): ")
    key = input("Enter Key (10 bits): ")
    if len(pt)!=8 or len(key)!=10:
        print("Invalid input")
        return
    print("\n--- S-DES Parameters ---")
    print("P10:",P10)
    print("P8 :",P8)
    print("IP :",IP)
    p10 = permute(key,P10)
    L = left_shift(p10[:5])
    R = left_shift(p10[5:])
    K1 = permute(L+R,P8)
    print("\n1) Key Generation")
    print("K1 =",K1)
    ip = permute(pt,IP)
    L0 = ip[:4]
    R0 = ip[4:]
    print("\n2) Initial Permutation")
    print("L0 =",L0,"R0 =",R0)
    ep = permute(R0,EP)
    xor_out = xor(ep,K1)
    print("\n3) Expansion Permutation")
    print("EP XOR K1 =",xor_out)
    s0 = sbox(xor_out[:4],S0)
    s1 = sbox(xor_out[4:],S1)
    s_out = s0+s1
    print("\n4) SBOX Substitution")
    print("S0 =",s0," S1 =",s1)
    print("Output =",s_out)
    L1 = xor(L0,s_out)
    print("\n5) F Function")
    print("L1 =",L1)
    pre = L1 + R0
    ct = permute(pre,IP1)
    print("\n6) Inverse Permutation")
    print("Cipher Text =",ct)
