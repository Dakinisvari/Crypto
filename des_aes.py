RCON = [0x01,0x00,0x00,0x00]
def hex_to_bytes(h):
    return [int(h[i:i+2],16) for i in range(0,len(h),2)]
def print_word(label,word):
    print(label," ".join(format(x,'02x') for x in word))
def run_aes():
    key = input("Enter 128-bit key (32 hex characters): ")
    rounds = int(input("Enter number of rounds: "))
    if len(key)!=32:
        print("Invalid key length")
        return
    bytes_key = hex_to_bytes(key)
    W=[]
    for i in range(4):
        W.append(bytes_key[i*4:(i+1)*4])
    print("\nInitial Words")
    for i in range(4):
        print_word(f"W{i}:",W[i])
    print("\nRCON:", " ".join(format(x,'02x') for x in RCON))
    rot=[W[3][1],W[3][2],W[3][3],W[3][0]]
    print_word("\nAfter RotWord:",rot)
    sub=[SBOX[b] for b in rot]
    print("\nSubWord")
    for i in range(4):
        print(f"SBOX({format(rot[i],'02x')}) = {format(sub[i],'02x')}")
    g=[sub[i]^RCON[i] for i in range(4)]
    print_word("\ng(W3):",g)
    for i in range(4,4+rounds*4):
        W.append([0]*4)
        for j in range(4):
            if i%4==0:
                W[i][j]=W[i-4][j]^g[j]
            else:
                W[i][j]=W[i-1][j]^W[i-4][j]
        print_word(f"W{i}:",W[i])
